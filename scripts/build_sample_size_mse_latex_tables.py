"""
Build sample-size MSE tables from results/sample_size/statistics and write LaTeX.

Reads variance summary CSVs (and optionally XLSX) from:
  results/sample_size/statistics/{noise_type}/{func_type}/
Files: *{model_key}*uncertainties_summary_{Linear|Sinusoidal}_{noise_type}.csv (no _entropy).

Output: results/sample_size/sample_size_mse_tables.tex (for \\input from appendix/main).
Requires: \\usepackage{booktabs}
"""
from pathlib import Path
import numpy as np
import pandas as pd

PROJECT_ROOT = Path(__file__).resolve().parent.parent
STATS_BASE = PROJECT_ROOT / "results" / "sample_size" / "statistics"
OUT_TEX = PROJECT_ROOT / "results" / "sample_size" / "sample_size_mse_tables.tex"

CONDITIONS = [
    ("linear", "homoscedastic", "Linear", "Homoscedastic"),
    ("linear", "heteroscedastic", "Linear", "Heteroscedastic"),
    ("sin", "homoscedastic", "Sinusoidal", "Homoscedastic"),
    ("sin", "heteroscedastic", "Sinusoidal", "Heteroscedastic"),
]

MODEL_ORDER = ["Deep Ensemble", "MC Dropout", "BNN", "BAMLSS"]

MODEL_KEYS = {
    "Deep Ensemble": "Deep_Ensemble",
    "MC Dropout": "MC_Dropout",
    "BNN": "BNN",
    "BAMLSS": "BAMLSS",
}

FUNC_NAME_BY_TYPE = {
    "linear": "Linear",
    "sin": "Sinusoidal",
}

PERCENTAGES = [5, 10, 25, 50, 100]


def _round_val(x, ndec=4):
    if pd.isna(x):
        return "---"
    try:
        return str(round(float(x), ndec))
    except (TypeError, ValueError):
        return "---"


def load_variance_stats(noise_type: str, func_type: str) -> dict:
    """
    Load per-model variance summary DataFrames (with Percentage and MSE) for one setting.
    Returns dict mapping model display name -> DataFrame. Excludes _entropy files.
    """
    search_dir = STATS_BASE / noise_type / func_type
    if not search_dir.exists():
        return {}

    func_name = FUNC_NAME_BY_TYPE[func_type]
    out = {}

    for display_name, model_key in MODEL_KEYS.items():
        # Variance only: no _entropy in filename
        pattern_csv = f"*{model_key}*uncertainties_summary_{func_name}_{noise_type}.csv"
        pattern_xlsx = f"*{model_key}*uncertainties_summary_{func_name}_{noise_type}.xlsx"
        matches = list(search_dir.glob(pattern_csv)) + list(search_dir.glob(pattern_xlsx))
        # Exclude any that contain _entropy (e.g. from wrong glob)
        matches = [f for f in matches if "_entropy" not in f.stem]
        if not matches:
            continue
        latest = sorted(matches)[-1]
        try:
            df = pd.read_csv(latest) if latest.suffix.lower() == ".csv" else pd.read_excel(latest)
        except Exception:
            continue
        if "Percentage" not in df.columns or "MSE" not in df.columns:
            continue
        out[display_name] = df

    return out


def build_mse_table_latex(
    func_type: str,
    noise_type: str,
    func_title: str,
    noise_title: str,
    data_by_model: dict,
) -> str:
    """One table: rows = models, columns = 5%, 10%, 25%, 50%, 100%, cells = MSE."""
    if not data_by_model:
        return f"% No data for {func_type} {noise_type}\n"

    caption = (
        f"Sample size experiment — MSE by training percentage — {func_title}, {noise_title}."
    )
    label = f"tab:sample-mse-{func_type}-{noise_type}"

    header_cells = " & ".join(["Model"] + [f"{p}\\,\\%" for p in PERCENTAGES])
    lines = [
        "\\begin{table}[htbp]",
        "  \\centering",
        f"  \\caption{{{caption}}}",
        f"  \\label{{{label}}}",
        "  \\begin{tabular}{lccccc}",
        "  \\toprule",
        f"  {header_cells} \\\\",
        "  \\midrule",
    ]

    for model in MODEL_ORDER:
        df = data_by_model.get(model)
        row_cells = [model]
        if df is None:
            row_cells.extend(["---"] * len(PERCENTAGES))
        else:
            pct_vals = pd.to_numeric(df["Percentage"], errors="coerce")
            mse_vals = df["MSE"]
            for p in PERCENTAGES:
                r = df[np.isclose(pct_vals, float(p), rtol=1e-9)]
                if r.empty or "MSE" not in r.columns:
                    row_cells.append("---")
                else:
                    row_cells.append(_round_val(r["MSE"].iloc[0]))
        lines.append("  " + " & ".join(row_cells) + " \\\\")

    lines.extend([
        "  \\bottomrule",
        "  \\end{tabular}",
        "\\end{table}",
        "",
    ])
    return "\n".join(lines)


def main():
    OUT_TEX.parent.mkdir(parents=True, exist_ok=True)
    parts = [
        "% Auto-generated by scripts/build_sample_size_mse_latex_tables.py",
        "% Sample size regression: MSE by training percentage from results/sample_size/statistics",
        "% Requires: \\usepackage{booktabs}",
        "",
    ]

    for func_type, noise_type, func_title, noise_title in CONDITIONS:
        data_by_model = load_variance_stats(noise_type, func_type)
        parts.append(f"% --- {func_title}, {noise_title} ---")
        parts.append(build_mse_table_latex(
            func_type, noise_type, func_title, noise_title, data_by_model
        ))

    tex = "\n".join(parts)
    OUT_TEX.write_text(tex, encoding="utf-8")
    print("Wrote:", OUT_TEX)


if __name__ == "__main__":
    main()
