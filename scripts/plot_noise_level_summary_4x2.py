"""
Create 4x2 noise-level summary panel plots for regression experiments.

For each combination of:
- function type: linear / sin
- noise type: homoscedastic / heteroscedastic
- measure: variance / entropy

we create a 4 (models) x 2 (rows: average uncertainties, correlation) panel:
- Columns: Deep Ensemble, MC Dropout, BNN, BAMLSS
- Row 1: normalized average aleatoric & epistemic uncertainties vs. noise level (tau)
- Row 2: correlation between epistemic and aleatoric uncertainty vs. noise level (tau)

Additionally we save two 1×4 appendix figures per condition: AU/EU (and Total when present),
and correlation only, named ``noise_level_summary_1x4_au_eu_*`` and ``noise_level_summary_1x4_corr_*``.

Normalized AU/EU/(Total) rows use a fixed y-axis ``[0, 1.5]``. Correlation row remains approximately ``[-1, 1]``.

Statistics are read from CSVs in results/noise_level/statistics, generated by the
noise level experiments and saved via utils.results_save.save_summary_statistics*
functions. Existing plotting/statistics code is left untouched; this script is
an add-on to produce summary figures for the thesis.
"""

import sys
from pathlib import Path
from typing import Dict, Literal, Optional

import matplotlib.pyplot as plt
import pandas as pd

project_root = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(project_root))

from utils.regression_summary_panels import (
    FUNC_NAME_BY_TYPE,
    MODEL_KEYS,
    NORMALIZED_UNCERTAINTY_YLIM,
    create_noise_level_summary_panel,
    create_noise_level_summary_panel_au_eu_only,
    create_noise_level_summary_panel_corr_only,
)
from utils.results_save import save_plot

FuncType = Literal["linear", "sin"]
NoiseType = Literal["homoscedastic", "heteroscedastic"]
MeasureType = Literal["variance", "entropy"]

PROJECT_ROOT = Path(__file__).resolve().parent.parent
STATS_BASE = PROJECT_ROOT / "results" / "noise_level" / "statistics"


def _find_stats_csv(
    func_type: FuncType,
    noise_type: NoiseType,
    measure: MeasureType,
    model_key: str,
    distribution: str = "normal",
) -> Optional[Path]:
    """
    Find the most recent noise-level statistics CSV for a given combination.

    Looks under:
        results/noise_level/statistics/{noise_type}/{func_type}/[distribution]/
    and matches files of the form:
        *{model_key}*uncertainties_summary_{FunctionName}_{noise_type}_{distribution}[ _entropy].csv
    """
    base_dir = STATS_BASE / noise_type / func_type
    candidates = []

    if base_dir.exists():
        dist_dir = base_dir / distribution
        if dist_dir.exists():
            candidates.append(dist_dir)
        candidates.append(base_dir)
    else:
        return None

    func_name = FUNC_NAME_BY_TYPE[func_type]
    entropy_suffix = "_entropy" if measure == "entropy" else ""

    pattern = (
        f"*{model_key}*uncertainties_summary_{func_name}"
        f"_{noise_type}_{distribution}{entropy_suffix}.csv"
    )

    matches: list[Path] = []
    for d in candidates:
        if d.exists():
            matches.extend(sorted(d.glob(pattern)))

    if not matches:
        return None

    return matches[-1]


def load_noise_level_stats(
    func_type: FuncType,
    noise_type: NoiseType,
    measure: MeasureType,
    distribution: str = "normal",
) -> Dict[str, pd.DataFrame]:
    """
    Load per-model statistics DataFrames for a given (func, noise, measure) combo.

    Returns a dict mapping model display names to DataFrames. Models without
    available statistics are omitted.
    """
    stats_by_model: Dict[str, pd.DataFrame] = {}

    for display_name, model_key in MODEL_KEYS.items():
        csv_path = _find_stats_csv(
            func_type=func_type,
            noise_type=noise_type,
            measure=measure,
            model_key=model_key,
            distribution=distribution,
        )
        if csv_path is None:
            continue
        try:
            df = pd.read_csv(csv_path)
        except Exception as exc:  # pragma: no cover - defensive
            print(f"  Error reading {csv_path}: {exc}")
            continue

        if "Tau" not in df.columns:
            print(f"  Skipping {csv_path} (no 'Tau' column).")
            continue

        stats_by_model[display_name] = df

    return stats_by_model


def main():
    combinations = []
    for func_type in ("linear", "sin"):
        for noise_type in ("homoscedastic", "heteroscedastic"):
            for measure in ("variance", "entropy"):
                combinations.append(
                    (
                        func_type,  # type: ignore[arg-type]
                        noise_type,  # type: ignore[arg-type]
                        measure,  # type: ignore[arg-type]
                    )
                )

    for func_type, noise_type, measure in combinations:
        print(
            f"\n=== Processing noise level summary: func={func_type}, noise={noise_type}, measure={measure} ==="
        )
        stats_by_model = load_noise_level_stats(
            func_type,  # type: ignore[arg-type]
            noise_type,  # type: ignore[arg-type]
            measure,  # type: ignore[arg-type]
            distribution="normal",
        )
        if not stats_by_model:
            print("  No stats found for this combination, skipping.")
            continue

        fig = create_noise_level_summary_panel(
            stats_by_model,
            func_type,  # type: ignore[arg-type]
            noise_type,  # type: ignore[arg-type]
            measure,  # type: ignore[arg-type]
            uncertainty_ylim=NORMALIZED_UNCERTAINTY_YLIM,
        )

        measure_label = "variance" if measure == "variance" else "entropy"
        filename = f"noise_level_summary_4x2_{func_type}_{noise_type}_{measure_label}"

        subfolder = f"noise_level/summary_panels/{noise_type}/{func_type}"
        save_plot(fig, filename, subfolder=subfolder)
        plt.close(fig)

        fig_au = create_noise_level_summary_panel_au_eu_only(
            stats_by_model,
            func_type,  # type: ignore[arg-type]
            noise_type,  # type: ignore[arg-type]
            measure,  # type: ignore[arg-type]
            uncertainty_ylim=NORMALIZED_UNCERTAINTY_YLIM,
        )
        save_plot(
            fig_au,
            f"noise_level_summary_1x4_au_eu_{func_type}_{noise_type}_{measure_label}",
            subfolder=subfolder,
        )
        plt.close(fig_au)

        fig_corr = create_noise_level_summary_panel_corr_only(
            stats_by_model,
            func_type,  # type: ignore[arg-type]
            noise_type,  # type: ignore[arg-type]
            measure,  # type: ignore[arg-type]
        )
        save_plot(
            fig_corr,
            f"noise_level_summary_1x4_corr_{func_type}_{noise_type}_{measure_label}",
            subfolder=subfolder,
        )
        plt.close(fig_corr)


if __name__ == "__main__":
    main()
