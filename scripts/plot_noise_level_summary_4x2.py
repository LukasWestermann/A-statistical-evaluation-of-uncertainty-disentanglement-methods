"""
Create 4x2 noise-level summary panel plots for regression experiments.

For each combination of:
- function type: linear / sin
- noise type: homoscedastic / heteroscedastic
- measure: variance / entropy

we create a 4 (models) x 2 (rows: average uncertainties, correlation) panel:
- Columns: Deep Ensemble, MC Dropout, BNN, BAMLSS
- Row 1: normalized average aleatoric & epistemic uncertainties vs. noise level (tau)
- Row 2: correlation between epistemic and aleatoric uncertainty vs. noise level (tau)

Statistics are read from CSVs in results/noise_level/statistics, generated by the
noise level experiments and saved via utils.results_save.save_summary_statistics*
functions. Existing plotting/statistics code is left untouched; this script is
an add-on to produce summary figures for the thesis.
"""

import sys
from pathlib import Path
from typing import Dict, Literal, Optional, Tuple

import matplotlib.pyplot as plt
import numpy as np
import pandas as pd

project_root = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(project_root))

from utils.results_save import save_plot


FuncType = Literal["linear", "sin"]
NoiseType = Literal["homoscedastic", "heteroscedastic"]
MeasureType = Literal["variance", "entropy"]


PROJECT_ROOT = Path(__file__).resolve().parent.parent
STATS_BASE = PROJECT_ROOT / "results" / "noise_level" / "statistics"


MODEL_KEYS: Dict[str, str] = {
    "Deep Ensemble": "Deep_Ensemble",
    "MC Dropout": "MC_Dropout",
    "BNN": "BNN",
    "BAMLSS": "BAMLSS",
}

FUNC_NAME_BY_TYPE: Dict[FuncType, str] = {
    "linear": "Linear",
    "sin": "Sinusoidal",
}


def _find_stats_csv(
    func_type: FuncType,
    noise_type: NoiseType,
    measure: MeasureType,
    model_key: str,
    distribution: str = "normal",
) -> Optional[Path]:
    """
    Find the most recent noise-level statistics CSV for a given combination.

    Looks under:
        results/noise_level/statistics/{noise_type}/{func_type}/[distribution]/
    and matches files of the form:
        *{model_key}*uncertainties_summary_{FunctionName}_{noise_type}_{distribution}[ _entropy].csv
    """
    # Files are sometimes in a 'normal' subfolder and sometimes directly under func_type
    base_dir = STATS_BASE / noise_type / func_type
    candidates = []

    if base_dir.exists():
        # With explicit distribution subfolder
        dist_dir = base_dir / distribution
        if dist_dir.exists():
            candidates.append(dist_dir)
        # Also allow files directly under func_type (some entropy CSVs use that)
        candidates.append(base_dir)
    else:
        return None

    func_name = FUNC_NAME_BY_TYPE[func_type]
    entropy_suffix = "_entropy" if measure == "entropy" else ""

    pattern = (
        f"*{model_key}*uncertainties_summary_{func_name}"
        f"_{noise_type}_{distribution}{entropy_suffix}.csv"
    )

    matches: list[Path] = []
    for d in candidates:
        if d.exists():
            matches.extend(sorted(d.glob(pattern)))

    if not matches:
        return None

    return matches[-1]


def load_noise_level_stats(
    func_type: FuncType,
    noise_type: NoiseType,
    measure: MeasureType,
    distribution: str = "normal",
) -> Dict[str, pd.DataFrame]:
    """
    Load per-model statistics DataFrames for a given (func, noise, measure) combo.

    Returns a dict mapping model display names to DataFrames. Models without
    available statistics are omitted.
    """
    stats_by_model: Dict[str, pd.DataFrame] = {}

    for display_name, model_key in MODEL_KEYS.items():
        csv_path = _find_stats_csv(
            func_type=func_type,
            noise_type=noise_type,
            measure=measure,
            model_key=model_key,
            distribution=distribution,
        )
        if csv_path is None:
            continue
        try:
            df = pd.read_csv(csv_path)
        except Exception as exc:  # pragma: no cover - defensive
            print(f"  Error reading {csv_path}: {exc}")
            continue

        if "Tau" not in df.columns:
            print(f"  Skipping {csv_path} (no 'Tau' column).")
            continue

        stats_by_model[display_name] = df

    return stats_by_model


def _get_uncertainty_columns(
    df: pd.DataFrame,
    measure: MeasureType,
) -> Tuple[str, str, Optional[str]]:
    """
    Determine column names for AU, EU and (optionally) total uncertainty.
    """
    if measure == "variance":
        au_col = "Avg_Aleatoric_norm"
        eu_col = "Avg_Epistemic_norm"
        tot_col = "Avg_Total_norm" if "Avg_Total_norm" in df.columns else None
    else:
        au_col = "Avg_Aleatoric_Entropy_norm"
        eu_col = "Avg_Epistemic_Entropy_norm"
        tot_col = (
            "Avg_Total_Entropy_norm"
            if "Avg_Total_Entropy_norm" in df.columns
            else None
        )

    return au_col, eu_col, tot_col


def create_noise_level_summary_panel(
    stats_by_model: Dict[str, pd.DataFrame],
    func_type: FuncType,
    noise_type: NoiseType,
    measure: MeasureType,
):
    """
    Create a 4x2 panel:
      - Columns: models (Deep Ensemble, MC Dropout, BNN, BAMLSS)
      - Row 1: normalized avg AU/EU (and optionally Total) vs. Tau
      - Row 2: correlation (Epi–Ale) vs. Tau

    Adds small numeric labels next to points.
    """
    model_order = ["Deep Ensemble", "MC Dropout", "BNN", "BAMLSS"]

    fig, axes = plt.subplots(2, 4, figsize=(18, 8), sharex=True)

    for col, model_name in enumerate(model_order):
        ax_top = axes[0, col]
        ax_bot = axes[1, col]

        df = stats_by_model.get(model_name)
        if df is None or df.empty:
            ax_top.text(
                0.5,
                0.5,
                "No data",
                ha="center",
                va="center",
                transform=ax_top.transAxes,
                fontsize=11,
            )
            ax_bot.axis("off")
            ax_top.set_title(model_name, fontweight="bold", fontsize=11)
            continue

        taus = df["Tau"].values

        au_col, eu_col, tot_col = _get_uncertainty_columns(df, measure)
        corr_col = "Correlation_Epi_Ale"

        # Row 1: average uncertainties
        y_au = df[au_col].values
        y_eu = df[eu_col].values

        ax_top.plot(
            taus,
            y_au,
            "o-",
            markersize=6,
            linewidth=2,
            color="green",
            label="AU",
        )
        ax_top.plot(
            taus,
            y_eu,
            "s-",
            markersize=6,
            linewidth=2,
            color="orange",
            label="EU",
        )

        if tot_col is not None:
            y_tot = df[tot_col].values
            ax_top.plot(
                taus,
                y_tot,
                "^-",
                markersize=6,
                linewidth=2,
                color="blue",
                label="Total",
            )

        # Numeric labels for AU, EU, and correlation
        def _annotate_series(ax, xs, ys, fmt: str = "{:.2f}", dy_scale: float = 0.03):
            if len(xs) == 0:
                return
            y_min, y_max = np.min(ys), np.max(ys)
            span = max(y_max - y_min, 1e-6)
            for x, y in zip(xs, ys):
                dy = dy_scale * span
                ax.text(
                    x + 0.02 * (xs[-1] - xs[0] if xs[-1] != xs[0] else 1.0),
                    y + dy,
                    fmt.format(y),
                    fontsize=9,
                    alpha=0.9,
                    ha="left",
                    va="bottom",
                )

        _annotate_series(ax_top, taus, y_au)
        _annotate_series(ax_top, taus, y_eu)

        ax_top.set_ylabel("Norm. avg uncertainty", fontsize=9)
        ax_top.set_title(model_name, fontweight="bold", fontsize=11)
        ax_top.grid(True, alpha=0.3)

        # Row 2: correlation
        if corr_col in df.columns:
            y_corr = df[corr_col].values
            ax_bot.plot(
                taus,
                y_corr,
                "D-",
                markersize=6,
                linewidth=2,
                color="purple",
                label="Corr(EU, AU)",
            )
            _annotate_series(ax_bot, taus, y_corr, fmt="{:.2f}", dy_scale=0.03)
            ax_bot.set_ylim(-1.05, 1.05)
        else:
            ax_bot.text(
                0.5,
                0.5,
                "No correlation column",
                ha="center",
                va="center",
                transform=ax_bot.transAxes,
                fontsize=9,
            )

        ax_bot.set_xlabel("Noise level (τ)", fontsize=9)
        ax_bot.set_ylabel("Corr(EU, AU)", fontsize=9)
        ax_bot.axhline(0.0, color="gray", linestyle="--", linewidth=1, alpha=0.4)
        ax_bot.grid(True, alpha=0.3)

    # Common y-labels on left-most column
    axes[0, 0].set_ylabel("Norm. avg uncertainty", fontsize=9)
    axes[1, 0].set_ylabel("Corr(EU, AU)", fontsize=9)

    func_label = FUNC_NAME_BY_TYPE[func_type]
    measure_label = "Variance" if measure == "variance" else "Entropy"
    title = f"Noise level — {func_label} ({noise_type}) — {measure_label}"
    fig.suptitle(title, fontsize=14, fontweight="bold", y=0.98)
    plt.tight_layout(rect=[0, 0, 1, 0.96])

    # Add legends only once, in first column
    axes[0, 0].legend(fontsize=8, loc="best")
    axes[1, 0].legend(fontsize=8, loc="best")

    return fig


def main():
    combinations = []
    for func_type in ("linear", "sin"):
        for noise_type in ("homoscedastic", "heteroscedastic"):
            for measure in ("variance", "entropy"):
                combinations.append(
                    (
                        func_type,  # type: ignore[arg-type]
                        noise_type,  # type: ignore[arg-type]
                        measure,  # type: ignore[arg-type]
                    )
                )

    for func_type, noise_type, measure in combinations:
        print(
            f"\n=== Processing noise level summary: func={func_type}, noise={noise_type}, measure={measure} ==="
        )
        stats_by_model = load_noise_level_stats(
            func_type,  # type: ignore[arg-type]
            noise_type,  # type: ignore[arg-type]
            measure,  # type: ignore[arg-type]
            distribution="normal",
        )
        if not stats_by_model:
            print("  No stats found for this combination, skipping.")
            continue

        fig = create_noise_level_summary_panel(
            stats_by_model,
            func_type,  # type: ignore[arg-type]
            noise_type,  # type: ignore[arg-type]
            measure,  # type: ignore[arg-type]
        )

        measure_label = "variance" if measure == "variance" else "entropy"
        filename = f"noise_level_summary_4x2_{func_type}_{noise_type}_{measure_label}"

        # Save under results/noise_level/plots/noise_level/summary_panels/{noise}/{func}
        subfolder = f"noise_level/summary_panels/{noise_type}/{func_type}"
        save_plot(fig, filename, subfolder=subfolder)
        plt.close(fig)


if __name__ == "__main__":
    main()

