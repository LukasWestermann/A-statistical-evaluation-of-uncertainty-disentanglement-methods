#!/usr/bin/env python3
"""
Build LaTeX table(s) for label-noise classification: accuracy and AU--EU correlation
from ``*_eta_summary.xlsx`` under results/classification/label_noise/statistics.

Excel rows store list-valued columns as a single row (stringified lists); this script
parses them (including ``np.float32(...)`` fragments).

Usage::

    python scripts/export_label_noise_accuracy_correlation_latex.py
    python scripts/export_label_noise_accuracy_correlation_latex.py --out path/to/out.tex
"""

from __future__ import annotations

import argparse
import ast
import re
import sys
from pathlib import Path

import numpy as np
import pandas as pd

PROJECT_ROOT = Path(__file__).resolve().parent.parent
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))

STATS_ROOT = (
    PROJECT_ROOT
    / "results"
    / "classification"
    / "label_noise"
    / "statistics"
    / "classification"
    / "label_noise"
)

DEFAULT_OUT = (
    PROJECT_ROOT
    / "results"
    / "classification"
    / "label_noise"
    / "tables"
    / "label_noise_accuracy_correlation.tex"
)

def _discover_xlsx() -> list[tuple[str, Path]]:
    if not STATS_ROOT.is_dir():
        return []
    found: list[tuple[str, Path]] = []
    for p in sorted(STATS_ROOT.glob("*/*.xlsx")):
        if p.name.endswith("_eta_summary.xlsx"):
            key = p.parent.name  # e.g. mc_dropout_gl
            found.append((key, p))
    order_pref = [
        "mc_dropout_gl",
        "mc_dropout_it",
        "deep_ensemble_gl",
        "deep_ensemble_it",
        "bnn_gl",
        "bnn_it",
    ]
    by_name = {k: path for k, path in found}
    ordered = [(k, by_name[k]) for k in order_pref if k in by_name]
    for k, path in found:
        if k not in order_pref:
            ordered.append((k, path))
    return ordered


def _parse_list_cell(val) -> list[float]:
    if val is None or (isinstance(val, float) and np.isnan(val)):
        return []
    if isinstance(val, (list, tuple)):
        return [float(x) for x in val]
    if isinstance(val, np.ndarray):
        return [float(x) for x in val.tolist()]
    if isinstance(val, str):
        s = val.strip()
        if not s.startswith("["):
            return [float(s)]
        t = re.sub(r"np\.float(?:32|64)\s*\(([^)]+)\)", r"\1", s)
        parsed = ast.literal_eval(t)
        return [float(x) for x in parsed]
    return [float(val)]


def _model_decomp_label(sheet_key: str) -> tuple[str, str]:
    if sheet_key.endswith("_gl"):
        base = sheet_key[: -len("_gl")]
        decomp = "GL"
    elif sheet_key.endswith("_it"):
        base = sheet_key[: -len("_it")]
        decomp = "IT"
    else:
        return sheet_key, "?"
    pretty = {
        "mc_dropout": "MC Dropout",
        "deep_ensemble": "Deep Ensemble",
        "bnn": "BNN",
    }.get(base, base.replace("_", " ").title())
    return pretty, decomp


def _fmt_acc(x: float) -> str:
    return f"{x:.3f}"


def _fmt_float(x: float, nd: int = 4) -> str:
    return f"{x:.{nd}f}"


def build_tex(rows: list[tuple[str, str, float, float, float]]) -> str:
    lines = [
        "% Auto-generated by scripts/export_label_noise_accuracy_correlation_latex.py",
        "% Source: results/classification/label_noise/statistics/**/**_eta_summary.xlsx",
        "% Preamble: \\usepackage{booktabs}",
        "",
        r"\begin{table}[htbp]",
        r"  \centering",
        r"  \footnotesize",
        r"  \caption{Label-noise experiment: test accuracy and Pearson correlation $\rho_{\mathrm{AU,EU}}$ between aleatoric and epistemic uncertainty on the evaluation grid, by label-noise rate $\eta$.}",
        r"  \label{tab:label-noise-accuracy-correlation}",
        r"  \begin{tabular}{@{}llrrr@{}}",
        r"    \toprule",
        r"    Model & Decomp. & $\eta$ & Accuracy & $\rho_{\mathrm{AU,EU}}$ \\",
        r"    \midrule",
    ]
    current_model = None
    for model, decomp, eta, acc, rho in rows:
        if model != current_model:
            if current_model is not None:
                lines.append(r"    \addlinespace")
            current_model = model
        lines.append(
            f"    {model} & {decomp} & {eta:g} & {_fmt_acc(acc)} & {_fmt_float(rho)} \\\\"
        )
    lines.extend(
        [
            r"    \bottomrule",
            r"  \end{tabular}",
            r"\end{table}",
            "",
        ]
    )
    return "\n".join(lines)


def collect_rows() -> list[tuple[str, str, float, float, float]]:
    rows: list[tuple[str, str, float, float, float]] = []
    for sheet_key, path in _discover_xlsx():
        df = pd.read_excel(path, engine="openpyxl")
        if df.empty:
            continue
        r = df.iloc[0]
        etas = _parse_list_cell(r["eta"])
        acc = _parse_list_cell(r["Accuracy"])
        rho = _parse_list_cell(r["AU_EU_corr"])
        n = len(etas)
        if not (len(acc) == n and len(rho) == n):
            raise ValueError(
                f"Length mismatch in {path}: eta={n}, acc={len(acc)}, rho={len(rho)}"
            )
        model, decomp = _model_decomp_label(sheet_key)
        for j in range(n):
            rows.append((model, decomp, etas[j], acc[j], rho[j]))
    return rows


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--out", type=Path, default=DEFAULT_OUT, help="Output .tex path")
    args = parser.parse_args()

    rows = collect_rows()
    if not rows:
        print("No label-noise summary Excel files found; nothing written.")
        sys.exit(1)

    tex = build_tex(rows)
    out = args.out.resolve()
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(tex, encoding="utf-8")
    print(f"Wrote {out}")


if __name__ == "__main__":
    main()
