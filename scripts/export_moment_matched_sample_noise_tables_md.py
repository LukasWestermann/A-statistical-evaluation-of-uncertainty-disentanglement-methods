"""Export moment-matched sample_size / noise_level stats to one Markdown and one LaTeX file."""
from __future__ import annotations

import re
from pathlib import Path

import numpy as np
import pandas as pd

ROOT = Path(__file__).resolve().parent.parent
STATS = ROOT / "results" / "entropy_recomputed_moment_matched_batch" / "statistics"
OUT_DIR = ROOT / "results" / "entropy_recomputed_moment_matched_batch" / "tables"
MODELS = ["Deep_Ensemble", "MC_Dropout", "BNN", "BAMLSS"]
LABELS = {"Deep_Ensemble": "DeepEns", "MC_Dropout": "MCDrop", "BNN": "BNN", "BAMLSS": "BAMLSS"}
CONDITIONS = [
    ("linear", "homoscedastic"),
    ("linear", "heteroscedastic"),
    ("sin", "homoscedastic"),
    ("sin", "heteroscedastic"),
]


def _glob_latest(dir_path: Path, pattern: str) -> Path | None:
    found = sorted(dir_path.glob(pattern))
    return found[-1] if found else None


def load_sample_size(noise: str, func: str) -> dict[str, pd.DataFrame]:
    d = STATS / "sample_size" / noise / func
    out: dict[str, pd.DataFrame] = {}
    pats = [
        ("Deep_Ensemble", "*Deep_Ensemble*moment_matched_entropy_sample_size.xlsx"),
        ("MC_Dropout", "*MC_Dropout*moment_matched_entropy_sample_size.xlsx"),
        ("BNN", "*BNN*moment_matched_entropy_sample_size.xlsx"),
        ("BAMLSS", "*BAMLSS*moment_matched_entropy_sample_size.xlsx"),
    ]
    for key, pat in pats:
        p = _glob_latest(d, pat)
        if p is not None:
            out[key] = pd.read_excel(p)
    return out


def load_noise_level(noise: str, func: str, dist: str = "normal") -> dict[str, pd.DataFrame]:
    d = STATS / "noise_level" / noise / func
    out: dict[str, pd.DataFrame] = {}
    pats = [
        ("Deep_Ensemble", f"*Deep_Ensemble*moment_matched_entropy_noise_{dist}.xlsx"),
        ("MC_Dropout", f"*MC_Dropout*moment_matched_entropy_noise_{dist}.xlsx"),
        ("BNN", f"*BNN*moment_matched_entropy_noise_{dist}.xlsx"),
        ("BAMLSS", f"*BAMLSS*moment_matched_entropy_noise_{dist}.xlsx"),
    ]
    for key, pat in pats:
        p = _glob_latest(d, pat)
        if p is not None:
            out[key] = pd.read_excel(p)
    return out


def fmt4(x) -> str:
    return f"{float(x):.4f}"


def _slug(s: str) -> str:
    return re.sub(r"[^a-z0-9]+", "-", s.lower()).strip("-")


def _table_rows(
    data: dict[str, pd.DataFrame],
    xkey: str,
    present: list[str],
) -> tuple[list[float], list[list[str]]]:
    xs_set: set[float] = set()
    for df in data.values():
        xs_set.update(float(t) for t in df[xkey].unique())
    xs = sorted(xs_set)
    rows: list[list[str]] = []
    for x in xs:
        row: list[str] = []
        xv = float(x)
        row.append(str(int(xv)) if xv == int(xv) else str(xv))
        for mk in present:
            dfm = data[mk]
            m = dfm[np.isclose(dfm[xkey].astype(float), xv, rtol=0, atol=1e-9)]
            if len(m) == 0:
                row.extend(["n/a", "n/a", "n/a", "n/a"])
            else:
                r = m.iloc[0]
                row.extend(
                    [
                        fmt4(r["Avg_Aleatoric_Entropy_norm"]),
                        fmt4(r["Avg_Epistemic_Entropy_norm"]),
                        fmt4(r["Avg_Total_Entropy_norm"]),
                        fmt4(r["Correlation_Epi_Ale"]),
                    ]
                )
        rows.append(row)
    return xs, rows


def build_markdown() -> str:
    lines: list[str] = []
    blocks = [
        ("Sample size — Entropy (normalized)", "Percentage", True),
        ("Noise level (normal) — Entropy (normalized)", "Tau", False),
    ]
    for title, xkey, is_sample in blocks:
        lines.append(f"\n## {title}\n")
        for func, noise in CONDITIONS:
            data = load_sample_size(noise, func) if is_sample else load_noise_level(noise, func)
            scen = f"{func}, {noise}"
            if not data:
                lines.append(f"### {scen}\n\n*No Excel stats found.*\n")
                continue
            present = [m for m in MODELS if m in data]
            cols = [xkey]
            for mk in present:
                s = LABELS[mk]
                cols.extend([f"{s}_AU", f"{s}_EU", f"{s}_TU", f"{s}_rho"])
            _, rows = _table_rows(data, xkey, present)
            lines.append(f"### {scen}\n")
            lines.append("| " + " | ".join(cols) + " |")
            lines.append("|" + "|".join(["---"] * len(cols)) + "|")
            for row in rows:
                lines.append("| " + " | ".join(row) + " |")
            lines.append("")
    return "\n".join(lines).strip() + "\n"


def build_latex() -> str:
    """Single Overleaf-friendly fragment: \\usepackage{booktabs}; optional \\usepackage{graphicx} for resizebox."""
    out: list[str] = [
        "% Auto-generated by scripts/export_moment_matched_sample_noise_tables_md.py",
        "% Moment-matched recomputed entropy (min--max normalized AU/EU/TU; correlation).",
        "% In preamble: \\usepackage{booktabs} \\usepackage{graphicx}",
        "",
    ]
    blocks = [
        ("Sample size", "sample", "Percentage", True),
        ("Noise level (normal)", "noise", "Tau", False),
    ]
    xkey_first = {"Percentage": "Perc.", "Tau": r"$\tau$"}

    for block_title, block_slug, xkey, is_sample in blocks:
        out.append(f"% ========== {block_title} ==========")
        out.append(f"\\subsection{{{block_title} --- Entropy (normalized)}}")
        for func, noise in CONDITIONS:
            data = load_sample_size(noise, func) if is_sample else load_noise_level(noise, func)
            scen = f"{func}, {noise}"
            if not data:
                out.append(f"% No Excel stats: {scen}")
                continue
            present = [m for m in MODELS if m in data]
            _, rows = _table_rows(data, xkey, present)
            nmod = len(present)
            ncol = 1 + 4 * nmod
            spec = "l" + "r" * (ncol - 1)
            label = f"tab:entropy-norm-{block_slug}-{_slug(scen)}"
            cap = (
                f"{block_title}, Entropy (normalized): {scen.replace('_', r'\\_')}. "
                "AU, EU, TU: min--max normalized; $\\rho$: correlation of epistemic vs aleatoric entropy."
            )
            out.append(f"\\subsubsection{{{scen}}}")
            out.append("\\begin{table}[htbp]")
            out.append("  \\centering")
            out.append("  \\footnotesize")
            out.append("  \\setlength{\\tabcolsep}{2.5pt}")
            out.append(f"  \\caption{{{cap}}}")
            out.append(f"  \\label{{{label}}}")
            out.append(r"  \resizebox{\linewidth}{!}{%")
            out.append(f"  \\begin{{tabular}}{{@{{}}{spec}@{{}}}}")
            out.append(r"  \toprule")
            # header row 1: model groups
            h1 = [""]
            for mk in present:
                h1.append(f"\\multicolumn{{4}}{{c}}{{{LABELS[mk]}}}")
            out.append("  " + " & ".join(h1) + r" \\")
            cmid = []
            c = 2
            for _ in present:
                cmid.append(f"\\cmidrule(lr){{{c}-{c+3}}}")
                c += 4
            out.append("  " + " ".join(cmid))
            # header row 2
            h2 = [xkey_first[xkey]]
            for _ in present:
                h2.extend(["AU", "EU", "TU", r"$\rho$"])
            out.append("  " + " & ".join(h2) + r" \\")
            out.append(r"  \midrule")
            for row in rows:
                out.append("  " + " & ".join(row) + r" \\")
            out.append(r"  \bottomrule")
            out.append(r"  \end{tabular}")
            out.append(r"  }")
            out.append(r"\end{table}")
            out.append("")
    return "\n".join(out).rstrip() + "\n"


def main() -> None:
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    md_path = OUT_DIR / "moment_matched_sample_noise_overview.md"
    tex_path = OUT_DIR / "moment_matched_sample_noise_overview.tex"
    md_path.write_text(build_markdown(), encoding="utf-8")
    tex_path.write_text(build_latex(), encoding="utf-8")
    print("Wrote:", md_path)
    print("Wrote:", tex_path)


if __name__ == "__main__":
    main()
