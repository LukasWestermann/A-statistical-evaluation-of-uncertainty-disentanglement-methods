"""
Export one LaTeX table: sample-size experiment correlations (epistemic vs aleatoric).

- **Variance** ($\\rho_v$): `Correlation_Epi_Ale` from CSVs under
  ``results/sample_size/statistics`` (same files as ``plot_sample_size_summary_4x2`` /
  ``plot_summary_panels_consolidated`` variance summaries).
- **Entropy** ($\\rho_e$): `Correlation_Epi_Ale` from moment-matched batch Excel
  (same workbooks as ``load_sample_size_entropy_moment_matched`` /
  ``recompute_entropy_moment_matched_batch_from_npz``).

Rows: training percentage (union over available data per scenario).
Columns: per model, variance correlation then moment-matched entropy correlation.

Usage::

    python scripts/export_sample_size_correlation_var_entropy_latex.py
    python scripts/export_sample_size_correlation_var_entropy_latex.py \\
        --stats-mm path/to/entropy_recomputed_moment_matched_batch/statistics
"""
from __future__ import annotations

import argparse
import math
import runpy
import sys
from pathlib import Path
from typing import Dict, List, Optional, Set

import pandas as pd

project_root = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(project_root))

from utils.moment_matched_summary_loaders import load_sample_size_entropy_moment_matched
from utils.regression_summary_panels import MODEL_KEYS

_pss = runpy.run_path(
    str(project_root / "scripts" / "plot_sample_size_summary_4x2.py"),
    run_name="<load_sample_size_stats>",
)
load_sample_size_stats = _pss["load_sample_size_stats"]

MODEL_ORDER = list(MODEL_KEYS.keys())

CONDITIONS = [
    ("linear", "homoscedastic"),
    ("linear", "heteroscedastic"),
    ("sin", "homoscedastic"),
    ("sin", "heteroscedastic"),
]

COND_LABEL = {
    ("linear", "homoscedastic"): "Linear, homoscedastic",
    ("linear", "heteroscedastic"): "Linear, heteroscedastic",
    ("sin", "homoscedastic"): "Sinusoidal, homoscedastic",
    ("sin", "heteroscedastic"): "Sinusoidal, heteroscedastic",
}

MODEL_HEADER = {
    "Deep Ensemble": "DeepEns",
    "MC Dropout": "MCDrop",
    "BNN": "BNN",
    "BAMLSS": "BAMLSS",
}


def _corr_at_pct(df: Optional[pd.DataFrame], pct: float) -> Optional[float]:
    if df is None or df.empty or "Percentage" not in df.columns:
        return None
    col = "Correlation_Epi_Ale"
    if col not in df.columns:
        return None
    sub = df[pd.to_numeric(df["Percentage"], errors="coerce").sub(pct).abs() < 1e-6]
    if sub.empty:
        return None
    v = float(sub.iloc[0][col])
    if math.isnan(v):
        return None
    return v


def _collect_percentages(
    var_by: Dict[str, pd.DataFrame],
    mm_by: Dict[str, pd.DataFrame],
) -> List[float]:
    s: Set[float] = set()
    for d in (*var_by.values(), *mm_by.values()):
        if d is None or d.empty or "Percentage" not in d.columns:
            continue
        for x in pd.to_numeric(d["Percentage"], errors="coerce").dropna().unique():
            s.add(float(x))
    return sorted(s)


def _fmt_rho(x: Optional[float]) -> str:
    if x is None:
        return "---"
    return f"{x:.4f}"


def build_latex(mm_stats_root: Path) -> str:
    lines: List[str] = [
        "% Auto-generated by scripts/export_sample_size_correlation_var_entropy_latex.py",
        r"% $\rho_v$: epistemic--aleatoric correlation (variance), from sample_size CSV statistics.",
        r"% $\rho_e$: same for entropy, moment-matched recomputation (batch Excel).",
        r"% Preamble: \usepackage{booktabs}",
        "",
        r"\begin{table}[htbp]",
        r"  \centering",
        r"  \footnotesize",
        r"  \setlength{\tabcolsep}{3pt}",
        r"  \caption{Sample-size experiment: correlation of epistemic vs.\ aleatoric uncertainty along the evaluation grid, by training percentage. "
        r"$\rho_{\mathrm{var}}$ from variance summary CSVs; $\rho_{\mathrm{ent}}$ from moment-matched analytical entropy (same source as moment-matched summary panels).}",
        r"  \label{tab:sample-size-corr-variance-moment-entropy}",
    ]

    nmodel = len(MODEL_ORDER)
    # 1 (Perc.) + 2 * nmodel columns
    colspec = "l" + "rr" * nmodel
    lines.append(rf"  \begin{{tabular}}{{@{{}}{colspec}@{{}}}}")
    lines.append(r"    \toprule")
    head1 = [r" & \multicolumn{2}{c}{" + MODEL_HEADER[m] + r"}" for m in MODEL_ORDER]
    lines.append("    " + "Perc. (\\%)" + "".join(head1) + r" \\")
    cmid = []
    lo = 2
    for _ in MODEL_ORDER:
        hi = lo + 1
        cmid.append(rf"\cmidrule(lr){{{lo}-{hi}}}")
        lo = hi + 1
    lines.append("    " + " ".join(cmid))
    head2 = []
    for _ in MODEL_ORDER:
        head2.extend(
            [
                r"$\rho_{\mathrm{var}}$",
                r"$\rho_{\mathrm{ent}}$",
            ]
        )
    lines.append("    & " + " & ".join(head2) + r" \\")
    lines.append(r"    \midrule")

    for idx, (func_type, noise_type) in enumerate(CONDITIONS):
        scen = COND_LABEL[(func_type, noise_type)]
        var_by = load_sample_size_stats(func_type, noise_type, "variance")  # type: ignore[arg-type]
        mm_by = load_sample_size_entropy_moment_matched(mm_stats_root, func_type, noise_type)
        pcts = _collect_percentages(var_by, mm_by)
        lines.append(
            rf"    \multicolumn{{{1 + 2 * nmodel}}}{{@{{}}l}}{{\textit{{{scen}}}}} \\"
        )
        if not pcts:
            lines.append(r"    \multicolumn{" + str(1 + 2 * nmodel) + r"}{@{}l@{}}{No data.} \\")
            if idx < len(CONDITIONS) - 1:
                lines.append(r"    \addlinespace")
            continue
        for pct in pcts:
            pr = str(int(pct)) if abs(pct - round(pct)) < 1e-9 else str(pct)
            cells = [_fmt_rho(_corr_at_pct(var_by.get(m), pct)) for m in MODEL_ORDER]
            cells_e = [_fmt_rho(_corr_at_pct(mm_by.get(m), pct)) for m in MODEL_ORDER]
            interleaved = []
            for a, b in zip(cells, cells_e):
                interleaved.extend([a, b])
            lines.append(f"    {pr} & " + " & ".join(interleaved) + r" \\")
        if idx < len(CONDITIONS) - 1:
            lines.append(r"    \addlinespace")

    lines.append(r"    \bottomrule")
    lines.append(r"  \end{tabular}")
    lines.append(r"\end{table}")
    lines.append("")
    return "\n".join(lines)


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--stats-mm",
        type=Path,
        default=project_root
        / "results"
        / "entropy_recomputed_moment_matched_batch"
        / "statistics",
        help="Moment-matched batch statistics root (contains sample_size/.../*.xlsx)",
    )
    parser.add_argument(
        "--out",
        type=Path,
        default=project_root
        / "results"
        / "sample_size"
        / "tables"
        / "sample_size_correlation_variance_moment_entropy.tex",
    )
    args = parser.parse_args()
    mm_root = Path(args.stats_mm).resolve()
    out_path = Path(args.out).resolve()
    out_path.parent.mkdir(parents=True, exist_ok=True)
    tex = build_latex(mm_root)
    out_path.write_text(tex, encoding="utf-8")
    print("Wrote", out_path)


if __name__ == "__main__":
    main()
