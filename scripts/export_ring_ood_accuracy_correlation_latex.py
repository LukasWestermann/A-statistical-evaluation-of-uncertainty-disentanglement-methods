#!/usr/bin/env python3
"""
Build LaTeX table for ring--OOD classification: ring accuracy and AU--EU correlations
(ring training points vs gap region) from ``*_ring_ood_summary.xlsx``.
Does not include ECE.

Usage::

    python scripts/export_ring_ood_accuracy_correlation_latex.py
    python scripts/export_ring_ood_accuracy_correlation_latex.py --out path/to/out.tex
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path

import numpy as np
import pandas as pd

PROJECT_ROOT = Path(__file__).resolve().parent.parent
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))

STATS_ROOT = (
    PROJECT_ROOT
    / "results"
    / "classification"
    / "ring_ood"
    / "statistics"
    / "classification"
    / "ring_ood"
)

DEFAULT_OUT = (
    PROJECT_ROOT
    / "results"
    / "classification"
    / "ring_ood"
    / "tables"
    / "ring_ood_accuracy_correlation.tex"
)


def _discover_xlsx() -> list[tuple[str, Path]]:
    if not STATS_ROOT.is_dir():
        return []
    found: list[tuple[str, Path]] = []
    for p in sorted(STATS_ROOT.glob("*/*.xlsx")):
        if p.name.endswith("_ring_ood_summary.xlsx"):
            found.append((p.parent.name, p))
    order_pref = [
        "mc_dropout_gl",
        "mc_dropout_it",
        "deep_ensemble_gl",
        "deep_ensemble_it",
        "bnn_gl",
        "bnn_it",
    ]
    by_name = {k: path for k, path in found}
    ordered = [(k, by_name[k]) for k in order_pref if k in by_name]
    for k, path in found:
        if k not in order_pref:
            ordered.append((k, path))
    return ordered


def _model_decomp_label(sheet_key: str) -> tuple[str, str]:
    if sheet_key.endswith("_gl"):
        base = sheet_key[: -len("_gl")]
        decomp = "GL"
    elif sheet_key.endswith("_it"):
        base = sheet_key[: -len("_it")]
        decomp = "IT"
    else:
        return sheet_key, "?"
    pretty = {
        "mc_dropout": "MC Dropout",
        "deep_ensemble": "Deep Ensemble",
        "bnn": "BNN",
    }.get(base, base.replace("_", " ").title())
    return pretty, decomp


def _fmt_acc(x: float) -> str:
    return f"{x:.3f}"


def _fmt_float(x: float, nd: int = 4) -> str:
    return f"{x:.{nd}f}"


def _scalar(row: pd.Series, col: str) -> float:
    v = row[col]
    if v is None or (isinstance(v, float) and np.isnan(v)):
        return float("nan")
    return float(v)


def build_tex(rows: list[tuple[str, str, float, float, float]]) -> str:
    lines = [
        "% Auto-generated by scripts/export_ring_ood_accuracy_correlation_latex.py",
        "% Source: results/classification/ring_ood/statistics/**/**_ring_ood_summary.xlsx",
        "% Preamble: \\usepackage{booktabs}",
        "",
        r"\begin{table}[htbp]",
        r"  \centering",
        r"  \footnotesize",
        r"  \caption{Ring--OOD experiment: accuracy on ring training points and Pearson correlation $\rho_{\mathrm{AU,EU}}$ between normalized aleatoric and epistemic uncertainty on the ring ($\rho_{\mathrm{ring}}$) and in the gap region ($\rho_{\mathrm{gap}}$).}",
        r"  \label{tab:ring-ood-accuracy-correlation}",
        r"  \begin{tabular}{@{}llrrr@{}}",
        r"    \toprule",
        r"    Model & Decomp. & Accuracy & $\rho_{\mathrm{ring}}$ & $\rho_{\mathrm{gap}}$ \\",
        r"    \midrule",
    ]
    current_model = None
    for model, decomp, acc, rho_r, rho_g in rows:
        if model != current_model:
            if current_model is not None:
                lines.append(r"    \addlinespace")
            current_model = model
        lines.append(
            f"    {model} & {decomp} & {_fmt_acc(acc)} & {_fmt_float(rho_r)} & {_fmt_float(rho_g)} \\\\"
        )
    lines.extend(
        [
            r"    \bottomrule",
            r"  \end{tabular}",
            r"\end{table}",
            "",
        ]
    )
    return "\n".join(lines)


def collect_rows() -> list[tuple[str, str, float, float, float]]:
    rows: list[tuple[str, str, float, float, float]] = []
    for sheet_key, path in _discover_xlsx():
        df = pd.read_excel(path, engine="openpyxl")
        if df.empty:
            continue
        r = df.iloc[0]
        acc = _scalar(r, "acc_train")
        rho_r = _scalar(r, "au_eu_corr_train")
        rho_g = _scalar(r, "au_eu_corr_gap")
        model, decomp = _model_decomp_label(sheet_key)
        rows.append((model, decomp, acc, rho_r, rho_g))
    return rows


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--out", type=Path, default=DEFAULT_OUT, help="Output .tex path")
    args = parser.parse_args()

    rows = collect_rows()
    if not rows:
        print("No ring--OOD summary Excel files found; nothing written.")
        sys.exit(1)

    tex = build_tex(rows)
    out = args.out.resolve()
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(tex, encoding="utf-8")
    print(f"Wrote {out}")


if __name__ == "__main__":
    main()
