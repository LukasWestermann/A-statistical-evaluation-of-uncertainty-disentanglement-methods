def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--stats-mm",
        type=Path,
        default=project_root
        / "results"
        / "entropy_recomputed_moment_matched_batch"
        / "statistics",
        help="Moment-matched batch statistics root",
    )
    parser.add_argument(
        "--distribution",
        type=str,
        default="normal",
        help="Noise distribution tag in moment-matched filenames (default: normal).",
    )
    parser.add_argument(
        "--out",
        type=Path,
        default=project_root
        / "results"
        / "noise_level"
        / "tables"
        / "noise_level_correlation_moment_entropy.tex",
    )
    args = parser.parse_args()
    mm_root = Path(args.stats_mm).resolve()
    out_path = Path(args.out).resolve()
    out_path.parent.mkdir(parents=True, exist_ok=True)
    dist = str(args.distribution).strip().lower()
    tex = build_latex_correlation(mm_root, dist) + build_latex_mse(mm_root, dist)
    out_path.write_text(tex, encoding="utf-8")
    print("Wrote", out_path)


if __name__ == "__main__":
    main()
