r"""
Export two LaTeX tables for the **noise-level** ($\tau$) experiment.

1. **Correlations**: $\rho_{\mathrm{var}}$ from variance summary CSVs under
   ``results/noise_level/statistics`` (same as ``plot_noise_level_summary_4x2``);
   $\rho_{\mathrm{ent}}$ from moment-matched batch Excel
   (``load_noise_level_entropy_moment_matched`` / batch recomputation).

2. **MSE**: $\mathrm{MSE}_{\mathrm{var}}$ from those CSVs; $\mathrm{MSE}_{\mathrm{mm}}$ from the
   moment-matched workbooks (same as $\rho_{\mathrm{ent}}$).

Row key is **$\tau$** (noise scale). Default noise distribution filter: ``normal``.

Usage::

    python scripts/export_noise_level_correlation_moment_entropy_latex.py
    python scripts/export_noise_level_correlation_moment_entropy_latex.py --distribution normal \\
        --stats-mm results/entropy_recomputed_moment_matched_batch/statistics \\
        --out results/noise_level/tables/noise_level_correlation_moment_entropy.tex
"""
from __future__ import annotations

import argparse
import math
import runpy
import sys
from pathlib import Path
from typing import Dict, List, Optional, Set

import pandas as pd

project_root = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(project_root))

from utils.moment_matched_summary_loaders import load_noise_level_entropy_moment_matched
from utils.regression_summary_panels import MODEL_KEYS

_pss = runpy.run_path(
    str(project_root / "scripts" / "plot_noise_level_summary_4x2.py"),
    run_name="<load_noise_level_stats>",
)
load_noise_level_stats = _pss["load_noise_level_stats"]

MODEL_ORDER = list(MODEL_KEYS.keys())

CONDITIONS = [
    ("linear", "homoscedastic"),
    ("linear", "heteroscedastic"),
    ("sin", "homoscedastic"),
    ("sin", "heteroscedastic"),
]

COND_LABEL = {
    ("linear", "homoscedastic"): "Linear, homoscedastic",
    ("linear", "heteroscedastic"): "Linear, heteroscedastic",
    ("sin", "homoscedastic"): "Sinusoidal, homoscedastic",
    ("sin", "heteroscedastic"): "Sinusoidal, heteroscedastic",
}

MODEL_HEADER = {
    "Deep Ensemble": "DeepEns",
    "MC Dropout": "MCDrop",
    "BNN": "BNN",
    "BAMLSS": "BAMLSS",
}


def _fmt_tau(tau: float) -> str:
    if abs(tau - round(tau)) < 1e-9:
        return str(int(round(tau)))
    s = f"{tau:.10f}".rstrip("0").rstrip(".")
    return s if s else "0"


def _corr_at_tau(df: Optional[pd.DataFrame], tau: float) -> Optional[float]:
    if df is None or df.empty or "Tau" not in df.columns:
        return None
    col = "Correlation_Epi_Ale"
    if col not in df.columns:
        return None
    sub = df[pd.to_numeric(df["Tau"], errors="coerce").sub(tau).abs() < 1e-5]
    if sub.empty:
        return None
    v = float(sub.iloc[0][col])
    if math.isnan(v):
        return None
    return v


def _mse_at_tau(df: Optional[pd.DataFrame], tau: float) -> Optional[float]:
    if df is None or df.empty or "Tau" not in df.columns:
        return None
    col = "MSE"
    if col not in df.columns:
        return None
    sub = df[pd.to_numeric(df["Tau"], errors="coerce").sub(tau).abs() < 1e-5]
    if sub.empty:
        return None
    v = float(sub.iloc[0][col])
    if math.isnan(v):
        return None
    return v


def _collect_taus(
    var_by: Dict[str, pd.DataFrame],
    mm_by: Dict[str, pd.DataFrame],
) -> List[float]:
    s: Set[float] = set()
    for d in (*var_by.values(), *mm_by.values()):
        if d is None or d.empty or "Tau" not in d.columns:
            continue
        for x in pd.to_numeric(d["Tau"], errors="coerce").dropna().unique():
            s.add(float(x))
    return sorted(s)


def _fmt_rho(x: Optional[float]) -> str:
    if x is None:
        return "---"
    return f"{x:.4f}"


def _fmt_mse(x: Optional[float]) -> str:
    if x is None:
        return "---"
    if math.isnan(x):
        return "---"
    ax = abs(x)
    if ax != 0 and (ax >= 1e4 or ax < 1e-3):
        return f"{x:.4e}"
    return f"{x:.6g}"


def build_latex_correlation(
    mm_stats_root: Path,
    distribution: str,
) -> str:
    lines: List[str] = [
        "% Auto-generated by scripts/export_noise_level_correlation_moment_entropy_latex.py",
        rf"% Distribution filter for moment-matched Excel: {distribution}",
        r"% $\rho_{\mathrm{var}}$: correlation from noise_level variance CSVs.",
        r"% $\rho_{\mathrm{ent}}$: correlation from moment-matched entropy Excel.",
        r"% Preamble: \usepackage{booktabs}",
        "",
        r"\begin{table}[htbp]",
        r"  \centering",
        r"  \footnotesize",
        r"  \setlength{\tabcolsep}{3pt}",
        r"  \caption{Noise-level experiment ($\tau$ sweep): correlation of epistemic vs.\ aleatoric uncertainty along the evaluation grid. "
        r"$\rho_{\mathrm{var}}$ from variance summary CSVs; $\rho_{\mathrm{ent}}$ from moment-matched analytical entropy "
        rf"(distribution: \texttt{{{distribution}}})."
        r"}",
        r"  \label{tab:noise-level-corr-variance-moment-entropy}",
    ]

    nmodel = len(MODEL_ORDER)
    colspec = "l" + "rr" * nmodel
    lines.append(rf"  \begin{{tabular}}{{@{{}}{colspec}@{{}}}}")
    lines.append(r"    \toprule")
    head1 = [r" & \multicolumn{2}{c}{" + MODEL_HEADER[m] + r"}" for m in MODEL_ORDER]
    lines.append("    " + r"$\tau$" + "".join(head1) + r" \\")
    cmid = []
    lo = 2
    for _ in MODEL_ORDER:
        hi = lo + 1
        cmid.append(rf"\cmidrule(lr){{{lo}-{hi}}}")
        lo = hi + 1
    lines.append("    " + " ".join(cmid))
    head2 = []
    for _ in MODEL_ORDER:
        head2.extend(
            [
                r"$\rho_{\mathrm{var}}$",
                r"$\rho_{\mathrm{ent}}$",
            ]
        )
    lines.append("    & " + " & ".join(head2) + r" \\")
    lines.append(r"    \midrule")

    for idx, (func_type, noise_type) in enumerate(CONDITIONS):
        scen = COND_LABEL[(func_type, noise_type)]
        var_by = load_noise_level_stats(
            func_type, noise_type, "variance", distribution=distribution
        )  # type: ignore[arg-type]
        mm_by = load_noise_level_entropy_moment_matched(
            mm_stats_root, func_type, noise_type, distribution=distribution
        )
        taus = _collect_taus(var_by, mm_by)
        lines.append(
            rf"    \multicolumn{{{1 + 2 * nmodel}}}{{@{{}}l}}{{\textit{{{scen}}}}} \\"
        )
        if not taus:
            lines.append(r"    \multicolumn{" + str(1 + 2 * nmodel) + r"}{@{}l@{}}{No data.} \\")
            if idx < len(CONDITIONS) - 1:
                lines.append(r"    \addlinespace")
            continue
        for tau in taus:
            tr = _fmt_tau(tau)
            cells = [_fmt_rho(_corr_at_tau(var_by.get(m), tau)) for m in MODEL_ORDER]
            cells_e = [_fmt_rho(_corr_at_tau(mm_by.get(m), tau)) for m in MODEL_ORDER]
            interleaved = []
            for a, b in zip(cells, cells_e):
                interleaved.extend([a, b])
            lines.append(f"    {tr} & " + " & ".join(interleaved) + r" \\")
        if idx < len(CONDITIONS) - 1:
            lines.append(r"    \addlinespace")

    lines.append(r"    \bottomrule")
    lines.append(r"  \end{tabular}")
    lines.append(r"\end{table}")
    lines.append("")
    return "\n".join(lines)


